public class ExempleTccLcc{

    private int a;
    private int b;
    private int c;

    public ExempleTccLcc(){
        this.a=1;
        this.b=1;
        this.c=1;
    }

    public int addAAndB(){
        return this.a+this.b;
    }

    public int addAAndC(){
        return this.a+this.c;
    }

    public int addBAndC(){
        return this.b+this.c;
    }

}